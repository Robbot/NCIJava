
public class Repetitions {
	public static void main(String args[]){
		//for loop
		System.out.println("For Loop");
		for (int i=1;i<11;i++){
			System.out.println(i);
		}
		//while loop
		System.out.println("While Loop");
		int i=1;
		while (i<=10){
				System.out.println(i);
				i++;
		}
		//do while loop
		System.out.println("Do While Loop");
			int j =1;
				do{
					System.out.println(j);
					j+=1;
				}
			while(j<=10);
			
		}
	}
